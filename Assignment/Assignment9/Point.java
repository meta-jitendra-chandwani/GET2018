
public class Point {
	private float xPoint, yPoint;
	
	Point(float xPoint,float yPoint){
		this.setxPoint(xPoint);
		this.setxPoint(yPoint);
	}
	public float getxPoint() {
		return xPoint;
	}
	public void setxPoint(float xPoint) {
		this.xPoint = xPoint;
	}
	public float getyPoint() {
		return yPoint;
	}
	public void setyPoint(float yPoint) {
		this.yPoint = yPoint;
	}
}
