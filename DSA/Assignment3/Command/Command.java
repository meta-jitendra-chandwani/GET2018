package Command;
public enum Command {

	/**
	 * root - Root directory
	 * mkdir - make new directory
	 * cd - Change directory
	 * ls - list
	 * bk - back
	 * find - find path
	 * tree - structure of directory
	 * exit - exit from VCM
	 * cwd - current Working Directory
	 */
	root, mkdir, cd, bk, ls, find, tree, exit, cwd;

}
